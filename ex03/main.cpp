#include "HumanA.hpp"
#include "HumanB.hpp"
#include "Weapon.hpp"

int	main(void)
{
	{
		Weapon club("crude spiked club");
		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	{
		Weapon club("crude spiked club");
		HumanB jim("Jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}
	{
		HumanB steve("Steve");
		steve.attack();
		Weapon sword("rusty sword");
		steve.setWeapon(sword);
		steve.attack();
		sword.setType("shiny new sword");
		steve.attack();
	}
	return (0);
}
